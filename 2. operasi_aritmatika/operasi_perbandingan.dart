void main() {
  var a = 10;
  var b = 5;
  var c = 7;

  // operator operator perbandingan
  // tanda lebih besar
  print(a > b);
  // tanda lebih kecil
  print(a < b);
  // tanda sama dengan
  print(a = b);
  // lebih besar sama dengan >=
  print(a >= b);
  // lebih kecil sama dengan <=
  print(a <= b);
  // tanda sama dengan ==
  print(a == b);
  // tanda tidak sama dengan !=
  print(a != b);

  // tanda &&
  print((a > b) && (a < c));
  // tanda ||
  print((a > b) || (a > c));
}