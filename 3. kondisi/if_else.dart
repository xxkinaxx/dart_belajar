
void main() {
  int number = 10;
  if (number > 5) {
    print("Angka ${number} lebih besar dari angka 5");
  } else {
    print("Angka ${number} lebih kecil dari angka 5");
  }

  if (number % 2 == 0) {
    print("Angka ${number} adalah bilangan genap");
  } else {
    print("Angka ${number} adalah angka ganjil");
  }
}